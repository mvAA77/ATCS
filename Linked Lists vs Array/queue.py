from linkedlist import LinkedList

class Queue:
    
    queuee = LinkedList()
    
    
    def offer(self, elem):
        queuee.add(self.elem);
    
    def poll():
        return queuee.remove(0)
    
    
    def peek():
        return queuee.get(0)
    
    def isEmpty():
        return queuee.isEmpty()
    
    def size():
        return queuee.size()